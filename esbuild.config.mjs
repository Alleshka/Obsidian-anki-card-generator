import "dotenv/config";

import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";
import fs from "fs";
import path from "path";

const banner =
	`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = (process.argv[2] === "production");
const outDir = process.env.OBSIDIAN_PLUGIN_OUTPUT_DIR ?? null;

const copyToVaultPlugin = {
	name: "copy-to-vault",
	setup(build) {
		build.onEnd((result) => {
			if (result.errors.length > 0 || !outDir) return;

			fs.mkdirSync(outDir, { recursive: true });

			const filesToCopy = ["main.js", "manifest.json", "styles.css"];
			for (const file of filesToCopy) {
				const src = path.resolve(file);
				if (!fs.existsSync(src)) continue; // styles.css is optional
				fs.copyFileSync(src, path.join(outDir, file));
			}

			console.log(`[copy-to-vault] Copied to ${outDir}`);
		});
	},
};


const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
	minify: prod,
	plugins: [copyToVaultPlugin],
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
